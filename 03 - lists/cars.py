# Сотритровка sort()
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)
# Обратно сортировать
cars.sort(reverse =True)
print(cars)

# Временная сотрировка функции sorted()
cars = ['bmw', 'audi', 'toyota', 'subaru']
print('\nHere is the original list:')
print(cars)
print('\nHere is the sorted list:')
print(sorted(cars))
print("\nHere is the original list again:")
print(cars)

# Перевернул список методом reverse()
cars.reverse()
print(cars)

